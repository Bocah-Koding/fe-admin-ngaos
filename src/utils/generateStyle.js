export const handlingDashMenuStyle = (menuId) => {
    switch (menuId) {
        case 1:
            return "border-ngaos-1 bg-ngaos-1";
        case 2:
            return "border-ngaos-5 bg-ngaos-5";
        case 3:
            return "border-ngaos-3 bg-ngaos-3";
        case 4:
            return "border-ngaos-6 bg-ngaos-6";

        default:
            return "border-ngaos-1 bg-ngaos-1";
    }
};
